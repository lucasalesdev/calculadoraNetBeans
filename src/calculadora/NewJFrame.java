    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        valor2=Double.parseDouble(jTFResultado.getText());

        if(operacao == "soma")
        {
            jTHistorico.setText(jTHistorico.getText() +String.valueOf(valor1) + " + " + String.valueOf(valor2) +"="+String.valueOf(valor1+valor2)+ "\n");
            jTFResultado.setText(String.valueOf(valor1+valor2));
        }else if(operacao=="subtracao")
        {
            jTHistorico.setText(jTHistorico.getText() +String.valueOf(valor1) + " - " + String.valueOf(valor2) +"="+String.valueOf(valor1-valor2)+ "\n");
            jTFResultado.setText(String.valueOf(valor1-valor2));
        }else if(operacao=="multiplicacao"){
            jTHistorico.setText(jTHistorico.getText() +String.valueOf(valor1) + " x " + String.valueOf(valor2) +"="+String.valueOf(valor1*valor2)+ "\n");
            jTFResultado.setText(String.valueOf(valor1*valor2));
        }else if(operacao=="divisao"){
            if(valor2!= 0){
            jTHistorico.setText(jTHistorico.getText() +String.valueOf(valor1) + " / " + String.valueOf(valor2) +"="+String.valueOf(valor1/valor2)+ "\n");
            jTFResultado.setText(String.valueOf(valor1 / valor2));
            }else{
                showMessageDialog(null, "Indefinido, imposs√≠vel dividir um numero por zero ");
                jTFResultado.setText("");    
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

